void oledSetup() {
    if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
        for (;;);
    }
    
    display.clearDisplay();
    display.setTextSize(1);
    display.setTextColor(SSD1306_WHITE);
    display.setCursor(0, 0);
    display.println("系统启动中...");
    display.display();
    delay(2000);
}

void drawTimeAndDate() {
    display.setTextSize(2);
    String timeStr = "";
    if (currentHour < 10) timeStr += "0";
    timeStr += String(currentHour) + ":";
    if (currentMinute < 10) timeStr += "0";
    timeStr += String(currentMinute);
    
    int timeWidth = timeStr.length() * 12;
    display.setCursor((OLED_WIDTH - timeWidth) / 2, 0);
    display.print(timeStr);
    
    display.setTextSize(1);
    String dateStr = "";
    if (currentYear > 0) {
        dateStr += String(currentYear) + "/";
        if (currentMonth < 10) dateStr += "0";
        dateStr += String(currentMonth) + "/";
        if (currentDay < 10) dateStr += "0";
        dateStr += String(currentDay);
    } else {
        dateStr = "2024/01/01";
    }
    
    int dateWidth = dateStr.length() * 6;
    display.setCursor((OLED_WIDTH - dateWidth) / 2, 20);
    display.print(dateStr);
}

void drawSensorData() {
    display.setTextSize(1);
    int thirdLineY = 32;
    
    float temp = getTemperature();
    String tempStr = String(temp);
    display.setCursor(MARGIN, thirdLineY);
    display.print(tempStr);
    
    int degreeX = MARGIN + tempStr.length() * 6;
    display.drawCircle(degreeX, thirdLineY + 1, 1, SSD1306_WHITE);
    display.setCursor(degreeX + 3, thirdLineY);
    display.print("C");
    
    int light = getLightLevel();
    String lightStr = String(light) + "lux";
    display.setCursor(MARGIN + 54, thirdLineY);
    display.print(lightStr);
}

void drawIcon(int x, int y, const uint8_t* icon, bool connected, bool showCross) {
    display.drawBitmap(x, y, icon, 16, 16, SSD1306_WHITE);
    if (!connected || showCross) {
        display.drawLine(x + 2, y + 2, x + 14, y + 14, SSD1306_WHITE);
        display.drawLine(x + 14, y + 2, x + 2, y + 14, SSD1306_WHITE);
    }
}

void drawAllIcons() {
    int iconWidth = (OLED_WIDTH - 2 * MARGIN) / 4;
    extern bool g_autoMode;
    
    bool lightConn = isLightConnected();
    bool lightOn = isLightOn();
    drawIcon(MARGIN, ICONS_Y, lightOn ? ledIconOn : ledIconOff, lightConn, !lightConn);
    
    bool fanConn = isFanConnected();
    bool fanOn = isFanOn();
    drawIcon(MARGIN + iconWidth, ICONS_Y, fanOn ? fanIconOn : fanIconOff, fanConn, !fanConn);
    
    drawIcon(MARGIN + iconWidth * 2, ICONS_Y, g_autoMode ? autoIconOn : autoIconOff, true, !g_autoMode);
    
    const uint8_t* wifiIcon = wifiIconFull;
    bool wifiConn = networkConnected;
    
    if (wifiConn) {
        int rssi = WiFi.RSSI();
        if (rssi < -80) wifiIcon = wifiIconLow;
        else if (rssi < -70) wifiIcon = wifiIconMedium;
        else if (rssi < -50) wifiIcon = wifiIconHigh;
    }
    
    drawIcon(MARGIN + iconWidth * 3, ICONS_Y, wifiIcon, wifiConn, !wifiConn);
}

void updateDisplay() {
    display.clearDisplay();
    drawTimeAndDate();
    drawSensorData();
    drawAllIcons();
    display.display();
}

void oledLoop() {
    static unsigned long lastUpdate = 0;
    if (millis() - lastUpdate >= 1000) {
        lastUpdate = millis();
        updateDisplay();
    }
}
